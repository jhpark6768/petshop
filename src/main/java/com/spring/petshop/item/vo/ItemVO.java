package com.spring.petshop.item.vo;

public class ItemVO {
	private String itemno;
	private String i_name;
	private String i_price;
	private String i_class;
	private String i_basket;
	
	public ItemVO() {
		
	}
	
	public String getItemno() {
		return itemno;
	}
	public void setItemno(String itemno) {
		this.itemno = itemno;
	}
	public String getI_name() {
		return i_name;
	}
	public void setI_name(String i_name) {
		this.i_name = i_name;
	}
	public String getI_price() {
		return i_price;
	}
	public void setI_price(String i_price) {
		this.i_price = i_price;
	}
	public String getI_class() {
		return i_class;
	}
	public void setI_class(String i_class) {
		this.i_class = i_class;
	}
	public String getI_basket() {
		return i_basket;
	}
	public void setI_basket(String i_basket) {
		this.i_basket = i_basket;
	}
	
	
}
